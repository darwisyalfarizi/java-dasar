public class UnaryOperator{
    public static void main(String[]args){


      // ++  => a = a+1;
      // --  => a = a-1;
      // -  => Negative
      // +  => Positive
      // !  => Boolean kebalikan

        int d = 100;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);


    }
}