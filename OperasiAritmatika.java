public class OperasiAritmatika{
    public static void main(String[]args){


        int a =100;
        int b =9;

        

        System.out.println(a + b);

        System.out.println(a - b);

        System.out.println(a * b);

        System.out.println(a / b);

        System.out.println(a % b);

        //augmented assignment

        a += 1;
        System.out.println(a);
        a -= 1;
        System.out.println(a);
        a *= 1;
        System.out.println(a);
        a /= 1;
        System.out.println(a);
        a %= 1;




    }
}